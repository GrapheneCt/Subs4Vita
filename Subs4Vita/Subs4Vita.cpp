std::wstring GetParentProcessName(DWORD pid)
{
	HANDLE hSnapshot = CreateToolhelp32Snapshot(TH32CS_SNAPPROCESS, 0);
	if (hSnapshot == INVALID_HANDLE_VALUE)
	{
		return L"";
	}

	PROCESSENTRY32W pe = { sizeof(PROCESSENTRY32W) };
	DWORD parentPid = 0;
	if (Process32FirstW(hSnapshot, &pe))
	{
		do
		{
			if (pe.th32ProcessID == pid)
			{
				parentPid = pe.th32ParentProcessID;
				break;
			}
		} while (Process32NextW(hSnapshot, &pe));
	}

	CloseHandle(hSnapshot);

	if (parentPid == 0)
	{
		return L"";
	}

	hSnapshot = CreateToolhelp32Snapshot(TH32CS_SNAPPROCESS, 0);
	if (hSnapshot == INVALID_HANDLE_VALUE)
	{
		return L"";
	}

	std::wstring parentName;
	if (Process32FirstW(hSnapshot, &pe))
	{
		do
		{
			if (pe.th32ProcessID == parentPid)
			{
				parentName = pe.szExeFile;
				break;
			}
		} while (Process32NextW(hSnapshot, &pe));
	}

	CloseHandle(hSnapshot);

	return parentName;
}

uint32_t _htonl(uint32_t host)
{
	static const union { uint32_t i; char c[4]; } test = { 1 };
	if (test.c[0] == 1)
	{
		return ((host & 0xFF000000) >> 24) |
			((host & 0x00FF0000) >> 8) |
			((host & 0x0000FF00) << 8) |
			((host & 0x000000FF) << 24);
	}
	else
	{
		return host;
	}
}

bool isTtmlHeader(std::string const& string)
{
	const char* bom = "\xEF\xBB\xBF";
	size_t start = 0;
	if (string.size() >= 3 && std::memcmp(string.data(), bom, 3) == 0)
	{
		start = 3;
	}

	return string.find("<?xml", start) != std::string::npos;
}

